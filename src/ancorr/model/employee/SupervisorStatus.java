package ancorr.model.employee;


public class SupervisorStatus
{
    private int supervisorStatCode;
    private String supervisorStatDesc;

    public int getSupervisorStatCode()
    {
        return supervisorStatCode;
    }

    public void setSupervisorStatCode(int supervisorStatCode)
    {
        this.supervisorStatCode = supervisorStatCode;
    }

    public String getSupervisorStatDesc()
    {
        return supervisorStatDesc;
    }

    public void setSupervisorStatDesc(String supervisorStatDesc)
    {
        this.supervisorStatDesc = supervisorStatDesc;
    }
}
